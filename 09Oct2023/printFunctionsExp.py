name = "husein Bobade"
print(name);
#capitalize() will do the capitalization of First character of String
print(name.capitalize())
#o/p is : Husein bobade

#upper() will CAPITALIZE all character in string
print(name.upper())
#o/p is : HUSEIN BOBADE

#lower() will lowercase all character in string
print(name.lower())
#o/p is : husein bobade

#id() returns the reference of object
print(id(name));
#o/p: 2038777984752

